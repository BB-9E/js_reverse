// if...else 语句的操作比 if 语句多了一步:  当条件成立时，则执行 if 部分的代码块； 条件不成立时，则进入 else 部分。
public class Java条件语句之ifelse {
    public static void main(String[] args) {
        int age = 25;


        if (age >= 18) {
            System.out.println("成年了");
        } else {
            System.out.println("未成年了");
        }


    }
}
